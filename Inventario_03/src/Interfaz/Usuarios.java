    private void BotonAñadirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonAñadirMouseClicked
    

        if (TxtFieldNoIdentificacion.getText().isEmpty() || TxtFieldNombre.getText().isEmpty() || TxtFieldTelefono.getText().isEmpty() || TxtFieldCorreo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Datos incompletos");
        } 
        
        else {
            
            Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
//            String consulta = ("INSERT INTO USUARIO (NumIdentificacion, Nombre, Telefono, Correo) VALUES (?, ?, ?, ?);");  //Se realiza la consulta. Se van a incorporar los valores
String consulta = ("INSERT INTO USUARIO (NumIdentificacion, Nombre, Telefono, Correo) VALUES (?, ?, ?, ?);");  //Se real
            try {
            
            CallableStatement cs = conectar.estableceConexion().prepareCall(consulta);
            
            cs.setString(1, TxtFieldNoIdentificacion.getText());//Se llama a la variable
            cs.setString(2, TxtFieldNombre.getText());
            cs.setString(3, TxtFieldTelefono.getText());
            cs.setString(4, TxtFieldCorreo.getText());

            int row = cs.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Se AÑADIO correctamente");
            
            MostrarArticulos();
            
            Limpiar();
                
            } 
            catch (Exception e) {
                
                 JOptionPane.showMessageDialog(null, "ERROR al AÑADIR \n: " + e.toString());
                
            }
            
        }//else
    


    }//GEN-LAST:event_BotonAñadirMouseClicked

