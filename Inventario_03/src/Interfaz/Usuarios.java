    private void BotonEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonEditarMouseClicked
        
                if (TxtFieldNoIdentificacion.getText().isEmpty() || TxtFieldNombre.getText().isEmpty() || TxtFieldTelefono.getText().isEmpty() || TxtFieldCorreo.getText().isEmpty()) {
          
            JOptionPane.showMessageDialog(this, "Datos incompletos");
            
        } else {
            
            Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
            
            try {
                //numero_serie, marca, modelo, categoria, ubicacion
                String consulta = "UPDATE USUARIO SET Nombre ='"+TxtFieldNombre.getText()+"', " + 
                                                       "Telefono ='"+TxtFieldTelefono.getText()+"', " +
                                                       "Correo ='"+TxtFieldCorreo.getText()+"' " +
                                                       "WHERE NumIdentificacion = '"+TxtFieldNoIdentificacion.getText() + "' ";
 
                CallableStatement cs = conectar.estableceConexion().prepareCall(consulta); 
                
                
            cs.executeUpdate(consulta);   
            
            JOptionPane.showMessageDialog(this, "Usuario MODIFICADO correctamente");
            MostrarArticulos(); 
            Limpiar();
            
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, "ERROR al EDITAR usuario: \n" + e.toString());
                
            }
            
        }        
                
                
    }//GEN-LAST:event_BotonEditarMouseClicked

    private void TablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaUsuariosMouseClicked
  
        DefaultTableModel model = (DefaultTableModel)TablaUsuarios.getModel();
        int Indice = TablaUsuarios.getSelectedRow();
        
        TxtFieldNoIdentificacion.setText(model.getValueAt(Indice, 0).toString());
        TxtFieldNombre.setText(model.getValueAt(Indice, 1).toString());
        TxtFieldTelefono.setText(model.getValueAt(Indice, 2).toString());
        TxtFieldCorreo.setText(model.getValueAt(Indice, 3).toString());
        
        
    }//GEN-LAST:event_TablaUsuariosMouseClicked

    private void BotonBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonBorrarMouseClicked
        
        if (TxtFieldNoIdentificacion.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "INGRESE el usuario a ser BORRADO");
            
        } //if
        
        else{

            Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
            
            String key = TxtFieldNoIdentificacion.getText();
            
            try {
             
             String consulta = "DELETE FROM USUARIO WHERE NumIdentificacion =" + key;
                
//                Statement cs = conectar.createStatement();
             CallableStatement cs = conectar.estableceConexion().prepareCall(consulta); 
             
             cs.executeUpdate(consulta);
             
             MostrarArticulos(); 
             
             JOptionPane.showMessageDialog(this, "Usuario BORRADO");
             
             Limpiar();
               
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "ERROR al BORRAR usuario: \n" + e.toString());
            }
            
        }//else
        
    }//GEN-LAST:event_BotonBorrarMouseClicked

