    private void AñadirBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AñadirBotonMouseClicked

        Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
        
        String consulta = ("INSERT INTO ARTICULO (numero_serie, marca, modelo, categoria, ubicacion) VALUES (?, ?, ?, ?, ?);");  //Se realiza la consulta. Se van a incorporar los valores
        
        try {
            CallableStatement cs = conectar.estableceConexion().prepareCall(consulta);
            
            cs.setString(1, TxtFieldNumSerie.getText());//Se llama a la variable
            cs.setString(2, TxtFieldMarca.getText());
            cs.setString(3, TxtFieldModelo.getText());
            cs.setString(4, ComBoxCategoria.getSelectedItem().toString());
            cs.setString(5, ComBoxUbicacion.getSelectedItem().toString());

            int row = cs.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Se AÑADIO correctamente");
            
            MostrarArticulos();
            
            Limpiar();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR al AÑADIR: " + e.toString());
        }
        
        
    }//GEN-LAST:event_AñadirBotonMouseClicked

    private void BorrarBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BorrarBotonMouseClicked
       
        if (TxtFieldNumSerie.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "INGRESE el artículo a ser BORRADO");
            
        } //if
        
        else{

            Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
            
            String key = TxtFieldNumSerie.getText();
            
            try {
             
             String consulta = "DELETE FROM ARTICULO WHERE numero_serie =" + key;
                
//                Statement cs = conectar.createStatement();
             CallableStatement cs = conectar.estableceConexion().prepareCall(consulta); 
             
             cs.executeUpdate(consulta);
             
             MostrarArticulos(); 
             
             JOptionPane.showMessageDialog(this, "Artículo BORRADO");
             
             Limpiar();
               
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "ERROR al BORRAR artículo: " + e.toString());
            }
            
        }//else

    }//GEN-LAST:event_BorrarBotonMouseClicked

