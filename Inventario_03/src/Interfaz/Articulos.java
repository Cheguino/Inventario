    private void EditarBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditarBotonMouseClicked
        
        if (TxtFieldNumSerie.getText().isEmpty() || TxtFieldMarca.getText().isEmpty() || TxtFieldModelo.getText().isEmpty()) {
          
            JOptionPane.showMessageDialog(this, "Datos incompletos");
            
        } else {
            
            Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
            
            try {
                //numero_serie, marca, modelo, categoria, ubicacion
                String consulta = "UPDATE ARTICULO SET marca ='"+TxtFieldMarca.getText()+"', " + 
                                                       "modelo ='"+TxtFieldModelo.getText()+"', " +
                                                       "categoria ='"+ComBoxCategoria.getSelectedItem().toString()+"', " +
                                                       "ubicacion ='"+ComBoxUbicacion.getSelectedItem().toString()+"'" +
                                                       "WHERE numero_serie ="+TxtFieldNumSerie.getText();
 
                CallableStatement cs = conectar.estableceConexion().prepareCall(consulta); 
                
                
//            cs.setString(1, TxtFieldMarca.getText());
//            cs.setString(2, TxtFieldModelo.getText());
//            cs.setString(3, ComBoxCategoria.getSelectedItem().toString());
//            cs.setString(4, ComBoxUbicacion.getSelectedItem().toString());
//            cs.setString(5, TxtFieldNumSerie.getText()); 
                
            cs.executeUpdate(consulta);   
            
            JOptionPane.showMessageDialog(this, "Artículo MODIFICADO correctamente");
            MostrarArticulos(); 
            Limpiar();
            
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, "ERROR al EDITAR artículo: \n" + e.toString());
                
            }
            
        }
        
    }//GEN-LAST:event_EditarBotonMouseClicked

