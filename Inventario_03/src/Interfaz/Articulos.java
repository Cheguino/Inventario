    public void MostrarArticulos(){
        
       String sql = "SELECT * FROM ARTICULO; ";
        
        try {
            
            Conexion conectar = new Conexion(); //Se crea un objeto de la clase conexion para establecer la conexion
            
//            st = obConect.estableceConexion().createStatement(); 
            st = conectar.estableceConexion().createStatement();
            
            //Se ejecuta la consulta sql
             rs = st.executeQuery(sql);
             
             TablaArticulos.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "ERROR al MOSTRAR contenido de la tabla: \n" + e.toString());
            
        }
        
    }
   
